# UNIR TUPLAS
# Podemos unir dos o más tuplas mediante el uso del operador '+'

print('UNIR TUPLAS')
colors = ('red','green', 'blue')
fruits = ('apple', 'orange', 'cherry')
result = colors + fruits
print(result)


# MULTIPLICAR ITEMS DE LA TUPLA
# También podemos multiplicar el contenido de una tupla cuantas veces querramos.
# Lo realizamos mediante el operador '*'

print('MULTIPLICAR ITEMS EN UNA TUPLA')
colors = ('red','green', 'blue')
more_colors = colors * 2
print(more_colors)
